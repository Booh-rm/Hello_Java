package arithmetic_calculator;


public class cls_arithmetic {
    
    //------------------------------------------------------------------------------------------------------------------
    
    public static double sum_numbers(double number_one, double number_two){
        
        double sum = number_one + number_two;
        
        return sum;
    }
    
    //------------------------------------------------------------------------------------------------------------------
    
    public static double subtract_numbers(double number_one, double number_two){
        
        double subtract = number_one - number_two;
        
        return subtract;
    }
    
    //------------------------------------------------------------------------------------------------------------------
    
    public static double multiply_numbers(double number_one, double number_two){
        
        double multiplication = number_one * number_two;
        
        return multiplication;
    }
    
    //------------------------------------------------------------------------------------------------------------------
    
    public static double divide_numbers(double number_one, double number_two){
        
        double division = number_one / number_two;
        
        return division;
    }
    
    //------------------------------------------------------------------------------------------------------------------
    
}
